from __future__ import annotations

import json
from pathlib import Path


def _root() -> Path:
    return Path(__file__).resolve().parents[2]


def test_1000_user_readiness_scripts_exist() -> None:
    root = _root()
    assert (root / "eval" / "load_soak.py").exists()
    assert (root / "eval" / "run_readiness_gates.py").exists()
    assert (root / "eval" / "uat_scoreboard.py").exists()


def test_load_soak_supports_1000_users() -> None:
    text = (_root() / "eval" / "load_soak.py").read_text(encoding="utf-8")
    assert "choices=[10, 100, 1000]" in text
    assert "load_{args.users}_snapshot.json" in text
    assert "BORG MEMORY" in text or "UNTRUSTED HISTORICAL ADVICE" in text


def test_scoreboard_has_1000_user_gate() -> None:
    text = (_root() / "eval" / "uat_scoreboard.py").read_text(encoding="utf-8")
    assert "load_1000" in text
    assert "ready_for_1000" in text
    assert "LOAD_TEST_REPORT_1000" not in text  # report is generated by load runner, scoreboard consumes snapshot truth


def test_gate_runner_runs_1000_user_soak() -> None:
    text = (_root() / "eval" / "run_readiness_gates.py").read_text(encoding="utf-8")
    assert '"load_1000"' in text
    assert '"--users", "1000"' in text
    assert "ready_for_1000" in text


def test_latest_1000_snapshot_schema_if_present() -> None:
    path = _root() / "eval" / "load_1000_snapshot.json"
    if not path.exists():
        return
    data = json.loads(path.read_text(encoding="utf-8"))
    assert data["users"] == 1000
    assert "latency_ms" in data
    assert "p95" in data["latency_ms"]
    assert "p99" in data["latency_ms"]
    assert "passed" in data
